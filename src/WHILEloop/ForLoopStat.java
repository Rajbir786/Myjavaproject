
       package WHILEloop;
       public class ForLoopStat {
       	public static void main(String args[]) {
       
       		int n = 7;
       
       		for (int i = 1; i <= 10; i++) {
       			n = 7 * i;
       			System.out.println("7" + "*" + i + "=" + n);
       
       		}
       
       	}
       }